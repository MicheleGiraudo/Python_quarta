
# In  questo programma utilizziamo l'INDICIZZAZIONE delle stringhe

parola = input("Inserisci una parola: ")
# 0 è l'indice del primo carattere della stringa
print(f"Il primo carattere della parola è {parola[0]}")
print(f"Il secondo carattere della parola è {parola[1]}")

print(f"Il penultimo carattere della parola è {parola[-2]}")
print(f"L'ultimo carattere della parola è {parola[-1]}")